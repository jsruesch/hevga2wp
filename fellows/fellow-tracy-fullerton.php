          Tracy Fullerton, M.F.A., is an experimental game designer, professor and director of the USC Games program. Her research center, the Game Innovation Lab, has produced several influential independent games, including Cloud, flOw, Darfur is Dying, and The Night Journey, with artist Bill Viola. She is currently working on Walden, a simulation of Henry David Thoreau’s experiment at Walden Pond supported by the NEA and NEH. Tracy is the author of “Game Design Workshop: A Playcentric Approach to Creating Innovative Games,” a design textbook in use at game programs worldwide, and holder of the Electronic Arts Endowed Chair in Interactive Entertainment. Recent projects include Collegeology, a suite of college preparation games funded by the Department of Education, the Gilbert Foundation and the Gates Foundation and Reality Ends Here, an alternate reality game for incoming freshmen at the School of Cinematic Arts. Prior to joining USC, she was president and founder of the interactive television game developer, Spiderdance, Inc.  Spiderdance’s games included NBC’s Weakest Link, MTV’s webRIOT, The WB’s No Boundaries, History Channel’s History IQ, Sony Game Show Network’s Inquizition and TBS’s Cyber Bond.  Before starting Spiderdance, Tracy was a founding member of the New York design firm R/GA Interactive, Creative Director at the interactive film studio Interfilm and a designer at Robert Abel’s early interactive company Synapse. Tracy’s work has received numerous honors including an Emmy nomination for interactive television, Indiecade’s “Sublime Experience,” “Impact,” and “Trailblazer” awards, Games for Change “Game Changer” award, and the Game Developers Choice “Ambassador” Award. In September 2016, she was named the People’s Choice for most inspiring LA Woman in Los Angeles Magazine.
