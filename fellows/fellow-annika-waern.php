          Annika is a ‘research by design’ academic with a background in computer science and Human-Computer Interaction, who has dedicated the latest ten years of her life to understanding games and play, and more specifically, pervasive games. These are games that are played in the physical world, often but not necessarily with the aid of mobile and ubiquitous technology. During 2004-2008, she acted as the coordinator of IPerG, Integrated Project on Pervasive Games, an EU-funded project with a total budget of 10 M Euro and nine partners spread around Europe. Together with colleagues she co-authored the book Pervasive Games: Theory and design. In 2007-2013 she was one of four research leaders of the Mobile Life Excellence Centre at Stockholm University. From April 2012 - March 2013 she acted as Centre director, ending the position to take up the professorship at Uppsala University.<br /><br />
