          R. Michael Young is a Professor in the School of Computing and Deputy Director of the Entertainment Arts and Engineering Program at the University of Utah in Salt Lake City, UT, where he directs the Liquid Narrative research group.  Together with students and research staff, he works to develop  computational models of interactive narrative with applications to computer games, educational and training systems and virtual environments.  He is actively engaged in leadership activities with games industry professional and trade organizations at the national, state and local levels.  Michael has published more than 145 scientific papers in the venues including leading conferences  and journals in computer games, artificial intelligence, computational linguistics, autonomous agents and intelligent user interfaces.  He has served to co-found and build several of the leading conferences in the area of computer games research (the AAAI Conference on Artificial Intelligence and Interactive Entertainment, the International Conference on the Foundations of Digital Games) and has served as program committee member or program or conference chair of more than 50 leading conferences across the areas of computer games, artificial intelligence, and virtual worlds. He serves as vice-president and is a founding board member of the Society for the Advancement of the Science of Digital Games (SASDG), a scientific society leading the community of scholars and practitioners advancing games research.  Michael served as editor-in-chief for the Journal of Game Development and serves or has served as a member of the editorial board of the leading journals in the area of games and AI research, including ACM Transactions on Intelligent Interactive Systems, IEEE Transactions on Computational Intelligence and AI Games, the International Journal of Gaming and Computer-Mediated Simulations and Advances in Cognitive Systems<br /><br />
          Michael joined the University of Utah in May of 2016 after working 16 years at North Carolina State University in Raleigh, NC.  At NC State, Young was a professor of Computer Science and University Faculty Scholar.    He was a GlaxoSmithKline Faculty Fellow in Public Policy and Public Engagement in 2010.  That year, his work with the North Carolina Department of Commerce and the state legislature contributed to the establishment of financial incentives in the state budget in support of the digital media industry.    He is an an ACM Distinguished Scientist and a senior member of both the Associate for the Advancement of Artificial Intelligence (AAAI) and the IEEE.  He is a member of the International Game Developers’ Association (IGDA),  and he served for four years on the board of the IGDA’s North Carolina Triangle chapter.  He is a founding board member of the Triangle Games Initiative, a trade organization promoting the NC Triangle games industry.
